def crlf(x):
    return 10 if (x == 13) else x

def unfailingArcs(fail,nlist):
    return [(crlf(char),st) for (char,st) in enumerate(nlist) if (st != fail)]

def step(st,err,delta,hist):
    pdf = hist[st]
    next = delta[st]
    d = dist.dist(pdf)
    index = d.fuzz_index(random.random())
    (ch,nxt) = next[index]
    ch = ch if (not err) else random.randint(0,255)
    pdf[index] += 1
    hist[st] = pdf
    return (ch,nxt,hist)

def run(start,fail,accepting,delta):
    delta = [[st if (accepting[st] != 1) else start for st in line] for line in delta ]
    delta = [ unfailingArcs(fail,delta[st]) for st in range(len(delta)) ]
    delta[fail] = [(10,start)]
    for (st,line) in enumerate(delta):
        print(str(st) + " : " + str(line))
    st = start
    hist = {}
    for (st,line) in enumerate(delta):
        hist[st] = [0 for _ in line]
    buffer = bytearray()
    bad = False
    cnt = 0
    while True:
        err = random.random() < 0.1 if bad else False
        (ch,st,hist) = step(st,err,delta,hist)
        buffer.append(ch)
        if (st == start):
            bad = random.random() < 0.25
            print(buffer.decode('cp437'))
            buffer = bytearray()
            cnt += 1
            if (cnt % 1000 == 0):
                for (st,pdf) in hist.items():
                    print(str(st) + " : " + str(pdf))

