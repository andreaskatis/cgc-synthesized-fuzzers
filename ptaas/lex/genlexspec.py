def unfailingArcs(fail,nlist):
    return [(char,st) for (char,st) in enumerate(nlist) if (st != fail)]

def generateRequirements(rand,start,fail,accepting,delta):
    ## remap the accepting state to the start state
    delta = [[st if (accepting[st] != 1) else start for st in line] for line in delta ]
    ## Give the failure state the same transitions as the start state
    delta[fail] = delta[start]
    delta = [ unfailingArcs(fail,delta[st]) for st in range(len(delta)) ]
    res = ''
    for (st,line) in enumerate(delta):
        nset = set([fail])
        d = len(line)
        for (n,(char,nxt)) in enumerate(line):
            if rand:
                plow  = (n * 1.0)/(d)
                phigh = (n + 1.0)/(d)
                res += '  req_{0}_{1}: ((pre_st == {0}) implies ((({3:.3f} <= r) and (r < {4:.3f})) implies ((in == {1}) and (st == {2}))))\n'.format(st,char,nxt,plow,phigh)
            else:
                res += '  req_{0}_{1}: ((pre_st == {0}) implies ((in == {1}) implies (st == {2})))\n'.format(st,char,nxt)
            nset.add(nxt)
        if rand:
            for (n,(char,nxt)) in enumerate(line):
                plow  = (- (n * 1.0)/(d)) + 0
                phigh = (- (n + 1.0)/(d)) + 0
                res += '  nreq_{0}_{1}: ((pre_st == {0}) implies ((({4:.3f} <= r) and (r < {3:.3f})) implies (st == {2})))\n'.format(st,char,nxt,plow,phigh)
        else:
            res += '  req_{0}: ((pre_st == {0}) implies ({1}))\n'.format(st,' or '.join(['(st == {0})'.format(nxt) for nxt in nset]))
    return res

def main():
    global DELTA
    parser = argparse.ArgumentParser(description="Generate LEXER Specicification")
    parser.add_argument('-r', '--random',
                        action='store_true',
                        help="Add a randomization input")
    args = parser.parse_args()
    rflag = 'r : real' if args.random else ''    
    print(SPEAR_SPEC.format(rflag,generateRequirements(args.random,0,1,ACCEPTING,DELTA)))

if __name__ == "__main__":
    sys.exit(main())
